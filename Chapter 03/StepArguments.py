# step Arguments
# 1 start args
# 11 stop args
# 1 step by default
print("Odd numbers:")

for i in range(1, 11, 2):
  print(i)

print("================")
print("Even numbers:")
for j in range(0, 11, 2):
  print(j)

print("================")
print("Backward printing numbers:")
for k in range(10, 0, -1):
  print(k)
print("================")

for m in range(10, -1, -1):
  print(m)
  
print("================")

for m in range(1, -11, -1):
  print(m)
