# break and continue Keyword

# 1 to 10 print
print("================Break statement================")
for i in range(1, 11):
   if i == 5:
     break;
   print(i)
   
   # 1 to 10 print
print("================Continue statement================")
for j in range(1, 11):
   if j == 5:
     continue;
   print(j)
   
