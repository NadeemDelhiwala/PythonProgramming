# if statements are used to make decisions in code. They allow you to execute certain blocks of code only if a certain condition is true.
# The basic syntax of an if statement is as follows:
# if condition:
#     # code to execute if condition is true
# You can also use an else statement to execute code if the condition is false: 
# if condition:
#     # code to execute if condition is true  
age=int(input("Enter your age: "));
if age>=14:
  print("Inside if statement");
  print("You are above 14 years old.");
  print("End of if statement.");