# List vs String

# Strings are immutable (string cannot be changed/modified)

# List are mutable
s = "string"
t = s.title()
print(s)
print(t)
# you cannot append the any character to the original string

# List are mutable (List can be changed/modified)

l = ['word1', 'word2', 'word3']
l.pop()
print(l)
l.append("word3")
print(l)
# you can append the any character to the original list

