# Looping in the list
fruits = ['orange', 'apple', 'pear', 'banana', 'kiwi']

# for loop

print("******** For Loop ********")
for fruit in fruits:
  print(fruit)

print("******** While Loop ********")
# while
i = 0  # list starting index is always 0
while i < len(fruits):
     print(fruits[i])
     i += 1 
