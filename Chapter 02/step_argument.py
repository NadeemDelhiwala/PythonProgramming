# slicing and selecting sub sequences
lang="Python Programming";
# syntax for slicing is string[start:end-1,step argument]
print("Harshit"[2:5]);
print("Harshit"[0::2]);
print("Harshit"[0::3]);
print("Harshit" [0:5:1]);
print("Harshit" [0:5:2]);
print("Harshit" [0:5:3]);
print("Back tracking");
print("Harshit"[5::-1]);
print("Harshit"[-1::-1]);
print("Nadeem"[-1::-1]);
print("Nadeem"[::-1]); #trick