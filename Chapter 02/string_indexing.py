#string indexing
language="Python";
#position of characters in a string starts from 0
print(language[0]); # p
print(language[1]); # y
print(language[2]); # t
print(language[3]); # h
print(language[4]); # o
print(language[5]); # n

print("\n ++++++++++++++++++ Negative indexing++++++++++++++++++++++++++");
print(language[-1]); # n
print(language[-2]); # o  
print(language[-3]); # h
print(language[-4]); # t  
print(language[-5]); # y
print(language[-6]); # p